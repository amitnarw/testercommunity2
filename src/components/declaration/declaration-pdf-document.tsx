function QuestionAnswer({
  number,
  question,
  answer,
  isCustom = false,
  isAdmin = false,
}: {
  number: number;
  question: string;
  answer: string;
  isCustom?: boolean;
  isAdmin?: boolean;
}) {
  const accentBar = isCustom
    ? styles.customAccentLeft
    : isAdmin
      ? styles.adminAccentLeft
      : styles.primaryAccentLeft;
  const badgeBg = isCustom
    ? colors.customAccent
    : isAdmin
      ? colors.textMuted
      : colors.primary;

  return (
    <View style={styles.qaCard} wrap={false}>
      <View style={accentBar} />
      <View style={styles.qaCardHeader}>
        <View style={[styles.qaNumberBadge, { backgroundColor: badgeBg }]}>
          <Text style={styles.qaNumberBadgeText}>{number}</Text>
        </View>
        <Text style={styles.qaQuestionText}>{question}</Text>
      </View>
      {answer ? (
        <Text style={styles.qaAnswerText}>{answer}</Text>
      ) : (
        <Text style={styles.qaAnswerEmpty}>No answer provided</Text>
      )}
    </View>
  );
}

