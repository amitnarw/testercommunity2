function renderStars(rating: number | null): string {
  if (rating === null) return "—";
  const full = Math.floor(rating);
  const half = rating - full >= 0.5;
  let stars = "★".repeat(full);
  if (half) stars += "☆";
  stars += "☆".repeat(Math.max(0, 5 - full - (half ? 1 : 0)));
  return stars;
}

